    public static class gradeListModel extends AbstractTableModel {

        private final String[] columns = {"Subject", "Grade"};
        private final List<Grade> gradeList;

        public gradeListModel(List<Grade> gradeList) {
            this.gradeList = gradeList;
        }

        @Override
        public int getRowCount() {
            return gradeList.size();
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
             switch(columnIndex) {
                 case 0:
                     return gradeList.get(rowIndex).getSubject().getName();
                 case 1:
                     return gradeList.get(rowIndex).getGrade();
                 default:
                     return "-";
            }
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (getValueAt(0,columnIndex) != null) {
                return (getValueAt(0,columnIndex).getClass());
            }else {
                return Object.class;
            }
        }
    }

